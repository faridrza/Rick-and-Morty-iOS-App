import UIKit

extension UIColor{
    static var mainGreen : UIColor{
        return UIColor(named: "MainGreen") ?? .systemGreen
    }
    static var customLightGreen : UIColor{
        return UIColor(named: "CustomLightGreen") ?? .systemGreen
    }
    static var customTeal : UIColor{
        return UIColor(named: "CustomTeal") ?? .systemGreen
    }
}
